# y realizar todas las operaciones
# basicas de una calculadora (+,-,*,/)
# y mostrar por pantalla el resultado
# 
# 

n1=int(input("Numero 1: "));
n2=int(input("Numero 2: "));

ope=n1+n2;
print(f"{n1} + {n2} = {ope}")

ope=n1-n2;
print(f"{n1} - {n2} = {ope}")

ope=n1*n2;
print(f"{n1} * {n2} = {ope}")

ope=n1/n2;
print(f"{n1} / {n2} = {ope}")
